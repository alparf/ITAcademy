package HomeWork3;

public abstract class AbstractCalculator {

    public abstract double summation(double a, double b);

    public abstract double subtraction(double a, double b);

    public abstract double multiplication(double a, double b);

    public abstract double division(double a, double b);

    public abstract double raiseTo(double a, int b);

    public abstract double absolute(double a);

    public abstract double squareRoot(double a);
}
